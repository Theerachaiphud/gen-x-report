class GXRTextConfig(PretrainedConfig):
    
    model_type: str = "gxr_text_model"
    keys_to_ignore_at_inference = ["past_key_values"]

    def __init__(
        self,
        vocab_size=32000,
        hidden_size=4096,
        intermediate_size=11008,
        num_hidden_layers=32,
        num_attention_heads=32,
        num_key_value_heads=None,
        hidden_act="silu",
        max_position_embeddings=2048,
        initializer_range=0.02,
        rms_norm_eps=1e-6,
        use_cache=True,
        pad_token_id=None,
        bos_token_id=1,
        eos_token_id=2,
        pretraining_tp=1,
        tie_word_embeddings=False,
        rope_theta=10000.0,
        rope_scaling=None,
        attention_bias=False,
        attention_dropout=0.0,
        mlp_bias=False,
        **kwargs,
    ):
        self.vocab_size = vocab_size
        self.max_position_embeddings = max_position_embeddings
        self.hidden_size = hidden_size
        self.intermediate_size = intermediate_size
        self.num_hidden_layers = num_hidden_layers
        self.num_attention_heads = num_attention_heads

        # for backward compatibility
        if num_key_value_heads is None:
            num_key_value_heads = num_attention_heads

        self.num_key_value_heads = num_key_value_heads
        self.hidden_act = hidden_act
        self.initializer_range = initializer_range
        self.rms_norm_eps = rms_norm_eps
        self.pretraining_tp = pretraining_tp
        self.use_cache = use_cache
        self.rope_theta = rope_theta
        self.rope_scaling = rope_scaling
        self.attention_bias = attention_bias
        self.attention_dropout = attention_dropout
        self.mlp_bias = mlp_bias

        # Validate the correctness of rotary position embeddings parameters
        # BC: if there is a 'type' field, move it to 'rope_type'.
        if self.rope_scaling is not None and "type" in self.rope_scaling:
            self.rope_scaling["rope_type"] = self.rope_scaling["type"]
        rope_config_validation(self)

        super().__init__(
            pad_token_id=pad_token_id,
            bos_token_id=bos_token_id,
            eos_token_id=eos_token_id,
            tie_word_embeddings=tie_word_embeddings,
            **kwargs,
        )

class GenXReportConfig(PretrainedConfig):
    
    model_type: str = "genxreport_model"
    
    def __init__(self, 
                vision_config=None, 
                qformer_config=None, 
                text_config=None, 
                num_query_tokens=32, 
                **kwargs
                ):
            super().__init__(**kwargs)

            self.architectures = kwargs.get("architectures", ["GenXReportModel"])
            self.auto_map = kwargs.get("auto_map", {
                "AutoConfig": "configuration_gen_x_report.GenXReportConfig",
                "AutoModel": "modeling_gen_x_report.GenXReportModel"
            })
            self.vision_config = vision_config
            self.qformer_config = qformer_config
            self.text_config = text_config
            self.use_decoder_only_language_model = True
            self.num_query_tokens = num_query_tokens
            self.initializer_factor = 1.0
            self.initializer_range = 0.02
            self.tie_word_embeddings = False
            self.is_encoder_decoder = False